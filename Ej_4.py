# Calcular el interés simple generado por una inversión.

capital = 15000
interes_anual = 0.08 # 8%
tiempo_años = 5

# Ganancia
ganancia = capital * interes_anual * tiempo_años

# Resultado
print("Depósito inicial:", capital)
print("La ganancia de la inversión es de: $", ganancia)
print("Total:", (capital + ganancia))